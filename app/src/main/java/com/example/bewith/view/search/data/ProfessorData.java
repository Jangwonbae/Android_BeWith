package com.example.bewith.view.search.data;

public class ProfessorData {
    public String name;
    public String department;
    public String tel;
    public String lab;
    public String email;


    public ProfessorData(String name, String department, String tel, String lab, String email){
        this.name=name;
        this.department=department;
        this.tel=tel;
        this.lab=lab;
        this.email=email;
    }
}